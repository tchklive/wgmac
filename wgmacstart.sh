#!/bin/bash 
#-=-=-=-=-=-=-=-=-=-=-= Install WireGuard Client on MacOS -=-=-=-=-=-=-=-=-=-=-=-=-=
#///////////////////////////////////////////////////////////////////////////////////
#********************** Clone WireGuard From Repository*****************************
git clone https://github.com/tchklive/wgmac

#///////////////////////////////////////////////////////////////////////////////////
#******************** Unzip WireGuard Application **********************************
cd wgmac
unzip WireGuard.app.zip

#///////////////////////////////////////////////////////////////////////////////////
#******************** Copy WireGuard To Applications folder ************************
cp -R WireGuard.app /Applications

#///////////////////////////////////////////////////////////////////////////////////
#********************* Remove Temporary folder *************************************
cd..
rmdir wgmac